package com.example;

public class App {
    public static void main(String[] args) {
        System.out.println("Hellooooooooooooooooooooooooooo, World!");
    }
}
public class WhileLoopExample {
    public static void main(String[] args) {
        int i = 1; // Initialisierung der Zählvariablen
        while (i <= 5) { // Bedingung der while-Schleife
            System.out.println("i: " + i); // Ausgabe des aktuellen Werts von i
            i++; // Erhöhung der Zählvariablen
        }
    }
}

